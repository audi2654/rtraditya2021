//C Control Flow assignment : For Loop
//Program to show working of Incrementing 'for' loop with Single Iterating Variable

#include <stdio.h>

int main(void)
{
    //var decl.
    int i;

    //code
    printf("\n\n");

    printf("Printing digits from 1 to 10\n");

    for (i = 1; i <= 10; i++)
    {
        printf("\t%d\n", i);
    }

    printf("\n\n");

    return(0);
}
